// Escreva um programa que recebe dois números inteiros e mostra o 
// resto da divisão Para calcular o resto use o operador %. Se você usar 
// código x = m%y em x ficaria o resto da divisão de m por y


var x, y, resto;

x = parseInt(prompt('Digite um valor: '));
y = parseInt(prompt('Digite um valor: '));

resto = x % y;

alert(resto);